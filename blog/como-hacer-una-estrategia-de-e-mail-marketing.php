                    <div class="cuerpo">
                        <h2>Pasos de una estrategia de e-mail marketing</h2>
                            <p>Las ventajas del <strong class="subrayado">e-marketing</strong> son muchas, pero es importante ejecutarlo de la forma adecuada. Con estos 12 pasos puedes construir una estrategia de email marketing que te lleve a tus objetivos.
                        <h3>Paso 1: Analiza la situación actual</h3>
                            <p>>Echa un vistazo crítico al estado actual de tus campañas de email marketing y analiza los resultados. La idea aquí es determinar las oportunidades y los desafíos en este canal de marketing digital, para hacer que tu plan sea más impactante.Estas son algunas de las preguntas que deberías plantearte:</p>
                            <ul class="ul">
                                <li>¿Qué he estado tratando de lograr con este enfoque actual?</li>
                                <li>¿Qué tan bien ha funcionando?</li>
                                <li>¿Cómo se vincula y contribuye la estrategia actual con los objetivos generales de marketing y de la organización?<li>
                                <li>¿Qué desafíos he enfrentado?</li>
                                <li>¿Qué procesos existen?</li>
                            </ul>
                        <h3>Paso 2: Define los objetivos de tus campañas de email marketing</h3>
                            <p>Esto es: ¿qué es exactamente lo que quieres lograr con el marketing por correo electrónico?
                            Tu <strong class="subrayado">email marketing</strong> debe tener objetivos específicos: puede ser enviar un correo masivo a todos tus contactos, enviar un descuento a un grupo de clientes más específico, etc. Estos objetivos deben respaldar los objetivos comerciales y de marketing generales.
                            Piensa en cada objetivo como un resultado deseado en específico. Los ejemplos típicos de objetivos de email marketing incluyen: aumentar la lista de suscriptores o contactos, mejorar las tasas de apertura y clics, generar ventas o promocionar un nuevo producto.
                            Tener objetivos te ayudará a elegir el contenido adecuado para incluir en tus correos electrónicos.
                            Tus objetivos representan los pasos a lo largo del camino. Asegúrate de que tus objetivos sean SMART (Specific, Measurable, Attainable, Realistic, Time-bound) específicos, medibles, alcanzables, realistas y con un límite de tiempo.Por ejemplo, si tu objetivo más amplio es aumentar tu lista de suscriptores, entonces un objetivo adecuado podría ser aumentar las suscripciones al newsletter en 1000 registros en un periodo de 12 meses.</p> 
